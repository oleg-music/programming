package git.olegmusic.common;

import java.io.Serializable;

/**
 * Перечисление EyeColor представляет возможные цвета глаз.
 * Используется для указания цвета глаз объекта Person.
 */
public enum EyeColor implements Serializable {
    GREEN,
    RED,
    BLACK,
    BLUE,
    YELLOW;
}
