package git.olegmusic.common;

import java.io.Serializable;

/**
 * Перечисление HairColor представляет возможные цвета волос.
 * Используется для указания цвета волос объекта Person.
 */
public enum HairColor implements Serializable {
    GREEN,
    BLACK,
    YELLOW,
    ORANGE,
    WHITE;
}
